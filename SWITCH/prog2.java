package SWITCH;

public class prog2 {
    public static void main(String[] args) {
        int pos = 1;

        switch (pos) {
            case 1:
                System.out.println("Primeiro lugar");
                break;

            case 2:
                System.out.println("Segundo lugar");
                break;

            case 3:
                System.out.println("Terceiro lugar");
                break;

            default:
                System.out.println("Não subiu ao pódio");
                break;
        }
    }
}


